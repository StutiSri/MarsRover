package org.tw.pojo;

public class RoverData {
    private String plateauUpperCoordinates;
    private String roverCoordinate;
    private String commands;

    public String getPlateauUpperCoordinates() {
        return plateauUpperCoordinates;
    }

    public void setPlateauUpperCoordinates(String plateauUpperCoordinates) {
        this.plateauUpperCoordinates = plateauUpperCoordinates;
    }

    public String getRoverCoordinate() {
        return roverCoordinate;
    }

    public void setRoverCoordinate(String roverCoordinate) {
        this.roverCoordinate = roverCoordinate;
    }

    public String getCommands() {
        return commands;
    }

    public void setCommands(String commands) {
        this.commands = commands;
    }
}
